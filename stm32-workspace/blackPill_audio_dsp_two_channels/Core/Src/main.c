void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	//Checking if the interrupt was caused by TIM3 (triggering conversions and data transfer)
	if (htim->Instance == TIM3)
	{
		//Setting the output sample's upper six bits to CCR1 (channel 1) and the lower six bits to CCR2 (channel 2) of TIM2
		htim2.Instance->CCR1 = (dac_buffer[dac_curr_sample_pos] & 0xFC0) >> 6;
		htim2.Instance->CCR2 = dac_buffer[dac_curr_sample_pos] & 0xF3;


		//Checking if processing of the ADC's samples is needed
		if (dac_curr_sample_pos == BUFFER_SIZE / 2) //Lower half of the ADC's buffer needs to be processed
		{
			//Setting pointers to the upper half of the ADC's and the DAC's buffers
			adc_process_ptr = &adc_buffer[0];
			dac_process_ptr = &dac_buffer[0];

			//Setting the flag that processing is needed
			adc_process = true;
		}
		else if (dac_curr_sample_pos == BUFFER_SIZE - 1) //Upper half of the ADC's buffer needs to be processed
		{
			//Setting pointers to the lower half of the ADC's and DAC's buffers
			adc_process_ptr = &adc_buffer[BUFFER_SIZE];
			dac_process_ptr = &dac_buffer[BUFFER_SIZE / 2];

			//Setting the flag that processing is needed
			adc_process = true;
		}


		//Incrementing the position of the current sample and wrapping around if the buffer's end was reached
		dac_curr_sample_pos++;
		if (dac_curr_sample_pos >= BUFFER_SIZE)
		{
			dac_curr_sample_pos = 0;
		}
	}
}

void process_data()
{
	//Separating the ADC data of the two channels into two input buffers for the filters
	for(uint16_t i = 0; i < BUFFER_SIZE; i += 2)
	{
		filter1_in[i / 2] = convert_adc_to_q15(adc_process_ptr[i]);
		filter2_in[i / 2] = convert_adc_to_q15(adc_process_ptr[i + 1]);
	}

	//Copying the data of one input buffer to the DAC's output buffer to build a passthrough
	for(uint16_t i = 0; i < BUFFER_SIZE / 2; i++)
	{
		dac_process_ptr[i] = convert_q15_to_dac(filter1_in[i]);
	}
}

//Function to convert 12-bit ADC data to q15_t
q15_t convert_adc_to_q15(uint16_t input) {
	return (q15_t)((input << 3) - 32768);
}

//Function to convert q15_t to 12-bit DAC data
uint16_t convert_q15_to_dac(q15_t input) {
	return (uint16_t)((input + 32768) >> 3);
}

